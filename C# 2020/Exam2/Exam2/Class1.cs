using System;
using System.Collections.Generic;
using System.Text;

namespace Exam2
{
    class Rectangle
    {
        public Rectangle(int width, int height)
        {
            Width = width;
            Height = height;
        }

        public int Width
        {
            get { return Width; }
            set { Width = value; }
        }

        public int Height
        {
            get { return Height; }
            set { Height = value; }
        }
}   }
